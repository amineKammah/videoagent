def get_voice_by_id(voice_id: str) -> dict | None:
    """Get voice info by ID."""
    for voice in GEMINI_TTS_VOICES:
        if voice["id"] == voice_id:
            return voice
    return None


def is_valid_voice(voice_id: str) -> bool:
    """Check if a voice ID is valid."""
    return get_voice_by_id(voice_id) is not None
